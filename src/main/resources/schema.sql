drop table if exists course CASCADE;
drop table if exists lecturer CASCADE;
drop table if exists student CASCADE;
drop table if exists subject CASCADE;
drop table if exists subject_lecturer CASCADE;
create table course (id bigint generated by default as identity, student_id integer, subject_id bigint, primary key (id));
create table lecturer (id integer generated by default as identity, age integer not null, name varchar(255), position varchar(255), primary key (id));
create table student (id integer generated by default as identity, birth_place varchar(255), date_of_birth timestamp, gpa double, name varchar(255), primary key (id));
create table subject (id bigint generated by default as identity, credit integer not null, exam boolean, hours_per_week integer not null, name varchar(255), primary key (id));
create table subject_lecturer (lecturer_id integer not null, subject_id bigint not null);
alter table course add constraint fk_course_student foreign key (student_id) references student;
alter table course add constraint fk_course_subject foreign key (subject_id) references subject;
alter table subject_lecturer add constraint fk_lecturer_subject foreign key (subject_id) references subject;
alter table subject_lecturer add constraint fk_subject_lecturer foreign key (lecturer_id) references lecturer;